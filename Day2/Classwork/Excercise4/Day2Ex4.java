import java.util.Scanner;
class day2Ex4
{
	public static void main(String[] args)
	{
		Scanner no = new Scanner(System.in);
		
		System.out.print("Input the values1 : ");
		boolean val1 = no.nextBoolean();
		System.out.print("Output1 : " +val1);
		System.out.println();
		
		System.out.print("Input the value2 : ");
		byte val2 = no.nextByte();
		System.out.print("Output2 : " +val2);
		System.out.println();
		
		System.out.print("Input the value3 : ");
		short val3 = no.nextShort();
		System.out.print("output3 : " +val3);
		System.out.println();
		
		System.out.print("Input the value4 : ");
		int val4 = no.nextInt();
		System.out.println("Output4 : " +val4);
		System.out.println();
		
		System.out.print("Input the value5 : ");
		float val5 = no.nextFloat();
		System.out.println("Output5 : " +val5);
		System.out.println();
		
		System.out.print("Input the value6 : ");
		double val6 = no.nextDouble();
		System.out.println("Output6 : " +val6);
		System.out.println();
		
		System.out.print("Input the val7 : ");
		long val7 = no.nextLong();
		System.out.println("Output 7 : " +val7);
		System.out.println();
		
	}
}
		
